    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String cin = cin_field.getText();
        
         try {
            ConnectionBD conn = new ConnectionBD();
            String sql = "select * from clients where cin_client=?";
            PreparedStatement preparedstm = conn.initRequetePreparee(sql, true, cin);
            ResultSet rs = preparedstm.executeQuery();
            
            if (rs.next()) {
                id_client = rs.getInt("id_client");
                cin_modification.setText(rs.getString("cin_client"));
                nom_modification.setText(rs.getString("nom_client"));
                prenom_modification.setText(rs.getString("prenom_client"));
                
                btnModifier.setEnabled(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "cin inexistant");
            }
            
            conn.disconnect();
        } catch (Exception e) {
            System.err.println(e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        
        String cin = cin_modification.getText();
        String nom = nom_modification.getText();
        String prenom = prenom_modification.getText();
        String nom_agence = liste_agences.getSelectedItem().toString();
        String get_id_agence_sql = "Select code_agence from agences where nom_agence= ?";

        String requete = "update clients set cin_client=?,nom_client=?,prenom_client=?,code_agence=? where id_client=?";
        try {
            ConnectionBD conn = new ConnectionBD();
            PreparedStatement preparedStm = conn.initRequetePreparee(get_id_agence_sql, true,nom_agence);
            ResultSet rs = preparedStm.executeQuery();
            if(rs.next())
            {
                preparedStm = conn.initRequetePreparee(requete, true, cin, nom, prenom,rs.getInt("code_agence"),id_client);
                preparedStm.executeUpdate();

                JOptionPane.showMessageDialog(null, "client modifi√©");

                //actualiser la table des clients
                VisionnerClientPanel.model.setRowCount(0);
                 try {
                    
                     String sql = "select * from clients";
                     rs = conn.Select(sql);

                    while (rs.next()) {
                         cin = rs.getString("cin_client");
                         nom = rs.getString("nom_client");
                         prenom = rs.getString("prenom_client");
                        double solde_courant = 0, solde_epargne = 0;

                        sql = "select * from comptes where id_client= ?";
                        PreparedStatement prpstm = conn.initRequetePreparee(sql, true, rs.getInt("id_client"));
                        ResultSet rsc = prpstm.executeQuery();
                        while (rsc.next()) {
                            double solde = rsc.getDouble("solde_compte");

                            sql = "select * from comptes_courants where code_compte=?";
                            prpstm = conn.initRequetePreparee(sql, true, rsc.getInt("code_compte"));
                            ResultSet rscc = prpstm.executeQuery();
                            if (rscc.next()) {
                                //compte courant
                                solde_courant = solde;

                            } else { //comptes epargne
                                solde_epargne = solde;
                            }
                        }
                        model.addRow(new Object[]{cin, nom, prenom, solde_courant, solde_epargne});
                    }

                        conn.disconnect();
                    } catch (Exception e) {
                        System.err.println(e);
                    }
                
            }
            

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        btnModifier.setEnabled(false);
    }//GEN-LAST:event_btnModifierActionPerformed


