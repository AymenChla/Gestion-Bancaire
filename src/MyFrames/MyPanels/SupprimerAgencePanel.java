    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int code = Integer.parseInt(code_field.getText());

        try {
            ConnectionBD conn = new ConnectionBD();
            String sql = "select * from agences where code_agence=?";
            PreparedStatement preparedstm = conn.initRequetePreparee(sql, true, code);
            ResultSet rs = preparedstm.executeQuery();

            if (rs.next()) {
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Nom: " + rs.getString("nom_agence") + "\nVille: " + rs.getString("ville") + "\nadresse: " + rs.getString("adresse"), "Warning", dialogButton);
                if (dialogResult == JOptionPane.YES_OPTION) {

                    //calcul√© nombre compte client
                    sql = "select count(*) as nbclient from clients where code_agence = ?";
                    preparedstm = conn.initRequetePreparee(sql, true, code);
                    rs = preparedstm.executeQuery();
                    if (rs.next()) {
                        //si le client n'as pas de compte
                        if (rs.getInt("nbclient") == 0) {
                            //suppression effective
                            sql = "delete from agences where code_agence=?";
                            preparedstm = conn.initRequetePreparee(sql, true, code);
                            preparedstm.executeUpdate();
                            

                            JOptionPane.showMessageDialog(null, "Agence supprimer");
                        } else {
                            //set agence inactif

                            sql = "update agences set inactif=1 where code_agence=?";
                            preparedstm = conn.initRequetePreparee(sql, true, code);
                            preparedstm.executeUpdate();
                            JOptionPane.showMessageDialog(null, "set Agence inactif");
                        }

                        //actualiser la liste des agences
                        //mettre a jour la liste de agences
                        String requete = "select * from agences";
                        try {

                             rs = conn.Select(requete);

                            FrameClient.nouveauClientPanel.getListe_agences().removeAllItems();
                            FrameClient.visionnerClientPanel.getListe_agence().removeAllItems();
                            FrameClient.modificationClientPanel.getListe_agences().removeAllItems();
                            while (rs.next()) {
                                FrameClient.nouveauClientPanel.getListe_agences().addItem(rs.getString("nom_agence"));
                                FrameClient.visionnerClientPanel.getListe_agence().addItem(rs.getString("nom_agence"));
                                FrameClient.modificationClientPanel.getListe_agences().addItem(rs.getString("nom_agence"));
                            }

                            conn.disconnect();
                        } catch (Exception e) {
                            System.err.println(e);
                        }
                        
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "code inexistant");
            }

            conn.disconnect();
        } catch (Exception e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


