    public void fill_table(String sql)
    {
        
        model.setRowCount(0);
        try {
            ConnectionBD conn = new ConnectionBD();
            
            ResultSet rs = conn.initRequetePreparee(sql,false,Integer.parseInt(code_field.getText())).executeQuery();
            if(rs.next())
            {
                do{
                    int code_compte = rs.getInt("code_compte");
                    String type_transaction = rs.getString("type_transaction");
                    String montant_transaction = rs.getString("montant_transaction");
                    Date date_transaction = rs.getDate("date_transaction");

                    model.addRow(new Object[]{code_compte, type_transaction, montant_transaction, date_transaction});
                }while (rs.next());
            }
            else JOptionPane.showMessageDialog(null, "Aucune transactions");
            

            conn.disconnect();
        } catch (Exception e) {
            System.err.println(e);
        }

        table_transactions.setModel(model);   
    }
